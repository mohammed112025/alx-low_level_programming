#include "function_pointers.h"

/**
 * Description: op_add - add two numbers
 * Input:
 *	@a: [int] --> first number
 *	@b: [int] --> second number
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}



/**
 * Description: op_sub - subtract of two elements
 * Input:
 *	@a: [int] --> first number
 *	@b: [int] --> second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}



/**
 * Description: op_mul - multiply two integers
 * Input:
 *	@a: [int] --> first number
 *	@b: [int] --> second number
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * Description: op_div - divide two integers
 * Input:
 *	@a: [int] --> first number
 *	@b: [int] --> second number
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}



/**
 * Description: op_mod - reminder of divistion of two integers
 * Input:
 *	@a: [int] --> first number
 *	@b: [int] --> second number
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
